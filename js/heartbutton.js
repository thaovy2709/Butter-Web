//select image element
const image = document.getElementById ('addLove');
//listen for the click event
//toggle variable
let toggle = true;
image.addEventListener ('click', function(){
    //toggle
    toggle = !toggle;
    if(toggle){
        image.src='/img/icon/addtowishlist.svg';
    }else{
        image.src='/img/icon/wishlist.svg';
    }
    // image.src = '/img/icon/wishlist.svg';
})

